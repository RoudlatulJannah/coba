package contoh;

public class buku {
	String judul;
    String penulis;
    int jumlah;

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public void setPenulis(String penulis) {
        this.penulis = penulis;
    }

    public void setJumlah(int jumlah) {
        this.jumlah = jumlah;
    }

    public String getJudul() {
        return judul;
    }

    public String getPenulis() {
        return penulis;
    }

    public int getJumlah() {
        return jumlah;
    }

}
